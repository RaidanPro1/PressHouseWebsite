export type Language = 'ar';
export type TranslationKey = keyof typeof translations.ar;

export const useTranslation = () => {
    return React.useCallback((key: TranslationKey, vars?: Record<string, string | number>): string => {
        let translation = translations.ar[key] || key;
        if (vars) {
            Object.keys(vars).forEach(varKey => {
                const value = vars[varKey];
                translation = translation.replace(`{${varKey}}`, String(value));
            });
        }
        return translation;
    }, []);
};
